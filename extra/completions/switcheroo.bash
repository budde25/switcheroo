        switcheroo)
            opts="-v -h -V --verbose --help --version execute device list add remove gui help"
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 1 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
