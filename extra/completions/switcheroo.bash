    local i cur prev opts cmds
    COMPREPLY=()
    cur="${COMP_WORDS[COMP_CWORD]}"
    prev="${COMP_WORDS[COMP_CWORD-1]}"
    cmd=""
    opts=""

    for i in ${COMP_WORDS[@]}
    do
        case "${i}" in
            "$1")
                cmd="switcheroo"
                ;;
            add)
                cmd+="__add"
                ;;
            device)
                cmd+="__device"
                ;;
            execute)
                cmd+="__execute"
                ;;
            gui)
                cmd+="__gui"
                ;;
            help)
                cmd+="__help"
                ;;
            list)
                cmd+="__list"
                ;;
            remove)
                cmd+="__remove"
                ;;
            *)
                ;;
        esac
    done

